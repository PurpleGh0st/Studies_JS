// Atribuição Simples

var a = 5 + 3 // r= 8
var b = a % 5 // r= 3
var c = 5 * b ** 2 // r= 45
var d = 10 - a/2 // r= 6
var e = 6 * 2/d // r= 2
var f = b % e + 4/e // r= 3

// Auto-atribuições

var N = 3 // N vale 3
N = N + 2 // somar os valores depois do recebe e substituir o valor de N pelo resultado
N += 2 // é uma forma simplificada de realizar a mesma operação acima, só valido quando N for receber ele mesmo

// Incremento 

var N = 10 
N ++ // uma forma ainda mais simples de realizar essas operações comumente usadas, o resultado seria 11
