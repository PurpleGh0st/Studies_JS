/*
x + y = adição
x - y = subtração
x * y = multiplicação
x / y = divisão real
x % y = resto da divisão inteira
x ** y = potência (x elevado a y)

Ordem de Precedência:
1° = ()
2° = **
3° = % , / e *
4° = - e +
*/

var n1 = 5
var n2 = 2

n1 + n2 // resultado = 7
n1 - n2 // resultado = 3
n1 * n2 // resultado = 10
n1 / n2 // resultado = 2.5
n1 % n2 // resultado = 1
n1 ** n2 // resultado = 25
