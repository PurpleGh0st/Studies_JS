// Loops Definidos = For
// Geralmente usado quando souber a quantidade de vezes que precisamos interar no código

/*
For(início; condição; controle) {
    bloco de código
    } 
*/

for(let i = 0; i < 10; i++){
    console.log(i);  // imprime de 0 a 9
}

// Enquanto a condição for verdadeira ele intera, após falsa ele sairá e continuará a execução do programa