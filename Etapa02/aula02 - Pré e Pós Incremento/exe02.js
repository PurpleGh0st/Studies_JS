//Atribuição junto de Operações
let n = 10
console.log(n) 

n += 10 // Ele somará 10 ao valor da variável n, resultando em 20.
console.log(n) 

n -= 5 // Ele subtrairá 5 do valor da variável n, resultando em 15.
console.log(n) 

n *= 3 // Ele multiplicará o valor da variável n por 3, resultando em 45.
console.log(n) 

n /= 2 // Ele dividirá o valor da variável n por 2, resultando em 22.5.
console.log(n) 